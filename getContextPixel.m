
function iTempDn =getContextPixel(uiDirMode,uiXYflag, iTempD)
g_aucDirDxDy = {
    {
        % dx/dy
        [0, 0]; [0, 0]; [0, 0]; [11, 2]; [2, 0];
        [11, 3]; [1, 0]; [93, 7]; [1, 1]; [93, 8];
        [1, 2]; [1, 3]; [0, 0]; [1, 3]; [1, 2];
        [93, 8]; [1, 1]; [93, 7]; [1, 0]; [11, 3];
        [2, 0]; [11, 2]; [4, 0]; [8, 0]; [0, 0];
        [8, 0]; [4, 0]; [11, 2]; [2, 0]; [11, 3];
        [1, 0]; [93, 7]; [1, 1]
    };
    {
        % dy/dx
        [0, 0]; [0, 0]; [0, 0]; [93, 8]; [1, 1];
        [93, 7]; [1, 0]; [11, 3]; [2, 0]; [11, 2];
        [4, 0]; [8, 0]; [0, 0]; [8, 0]; [4, 0];
        [11, 2]; [2, 0]; [11, 3]; [1, 0]; [93, 7];
        [1, 1]; [93, 8]; [1, 2]; [1, 3]; [0, 0];
        [1, 3]; [1, 2]; [93, 8]; [1, 1]; [93, 7];
        [1, 0]; [11, 3]; [2, 0]
    }
};
imult =  g_aucDirDxDy{uiXYflag+1,1}{uiDirMode+1,1}(1);
ishift =  g_aucDirDxDy{uiXYflag+1,1}{uiDirMode+1,1}(2);
iTempDn = floor((iTempD*imult)/(2^ishift));
end
